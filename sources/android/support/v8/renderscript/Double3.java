package android.support.v8.renderscript;

public class Double3 {
    public double x;
    public double y;
    public double z;

    public Double3() {
    }

    public Double3(double initX, double initY, double initZ) {
        this.x = initX;
        this.y = initY;
        this.z = initZ;
    }
}
