package android.support.v8.renderscript;

public class Double4 {
    public double w;
    public double x;
    public double y;
    public double z;

    public Double4() {
    }

    public Double4(double initX, double initY, double initZ, double initW) {
        this.x = initX;
        this.y = initY;
        this.z = initZ;
        this.w = initW;
    }
}
