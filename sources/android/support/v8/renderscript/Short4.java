package android.support.v8.renderscript;

public class Short4 {
    public short w;
    public short x;
    public short y;
    public short z;

    public Short4() {
    }

    public Short4(short initX, short initY, short initZ, short initW) {
        this.x = initX;
        this.y = initY;
        this.z = initZ;
        this.w = initW;
    }
}
