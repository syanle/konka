package android.support.v8.renderscript;

public class Float4 {
    public float w;
    public float x;
    public float y;
    public float z;

    public Float4() {
    }

    public Float4(float initX, float initY, float initZ, float initW) {
        this.x = initX;
        this.y = initY;
        this.z = initZ;
        this.w = initW;
    }
}
