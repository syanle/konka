package android.support.v8.renderscript;

public class Byte4 {
    public byte w;
    public byte x;
    public byte y;
    public byte z;

    public Byte4() {
    }

    public Byte4(byte initX, byte initY, byte initZ, byte initW) {
        this.x = initX;
        this.y = initY;
        this.z = initZ;
        this.w = initW;
    }
}
