package android.support.v8.renderscript;

public class Int4 {
    public int w;
    public int x;
    public int y;
    public int z;

    public Int4() {
    }

    public Int4(int initX, int initY, int initZ, int initW) {
        this.x = initX;
        this.y = initY;
        this.z = initZ;
        this.w = initW;
    }
}
